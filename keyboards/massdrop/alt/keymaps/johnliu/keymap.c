#define FOLLOW_TIMEOUT 3000
bool is_follow_held = false;
uint16_t follow_timer = 0;

// Runs just one time when the keyboard initializes.
void matrix_init_user(void) {
};

// Runs constantly in the background, in a loop.
void matrix_scan_user(void) {
    if (is_follow_held) {
        if (timer_elapsed(follow_timer) >= FOLLOW_TIMEOUT) {
            SEND_CMD("u");
            is_follow_held = false;
        }
    }
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case L_BRI:
            if (record->event.pressed) {
                if (LED_GCR_STEP > LED_GCR_MAX - gcr_desired) gcr_desired = LED_GCR_MAX;
                else gcr_desired += LED_GCR_STEP;
                if (led_animation_breathing) gcr_breathe = gcr_desired;
            }
            return false;
        case L_BRD:
            if (record->event.pressed) {
                if (LED_GCR_STEP > gcr_desired) gcr_desired = 0;
                else gcr_desired -= LED_GCR_STEP;
                if (led_animation_breathing) gcr_breathe = gcr_desired;
            }
            return false;
        case L_PTN:
            if (record->event.pressed) {
                if (led_animation_id == led_setups_count - 1) led_animation_id = 0;
                else led_animation_id++;
            }
            return false;
        case L_PTP:
            if (record->event.pressed) {
                if (led_animation_id == 0) led_animation_id = led_setups_count - 1;
                else led_animation_id--;
            }
            return false;
        case L_PSI:
            if (record->event.pressed) {
                led_animation_speed += ANIMATION_SPEED_STEP;
            }
            return false;
        case L_PSD:
            if (record->event.pressed) {
                led_animation_speed -= ANIMATION_SPEED_STEP;
                if (led_animation_speed < 0) led_animation_speed = 0;
            }
            return false;
        case L_T_MD:
            if (record->event.pressed) {
                led_lighting_mode++;
                if (led_lighting_mode > LED_MODE_MAX_INDEX) led_lighting_mode = LED_MODE_NORMAL;
            }
            return false;
        case L_T_ONF:
            if (record->event.pressed) {
                led_enabled = !led_enabled;
                I2C3733_Control_Set(led_enabled);
            }
            return false;
        case L_T_BR:
            if (record->event.pressed) {
                led_animation_breathing = !led_animation_breathing;
                if (led_animation_breathing) {
                    gcr_breathe = gcr_desired;
                    led_animation_breathe_cur = BREATHE_MIN_STEP;
                    breathe_dir = 1;
                }
            }
            return false;
        case L_T_PTD:
            if (record->event.pressed) {
                led_animation_direction = !led_animation_direction;
            }
            return false;
        case PC1:
            if (!record->event.pressed) {
                SEND_CMD("1");
            }
            return false;
        case PC2:
            if (record->event.pressed) {
                if (!is_follow_held) {
                    is_follow_held = true;
                    follow_timer = timer_read();
                }
            } else {
                if (timer_elapsed(follow_timer) < FOLLOW_TIMEOUT) {
                    SEND_CMD("2");
                }
                is_follow_held = false;
            }
            return false;
        default:
            return true; //Process all other keycodes normally
    }
}

layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case NIL:
          led_lighting_mode = LED_MODE_NORMAL;
          led_animation_breathing = false;
          gcr_desired = LED_GCR_MAX - 80;
          break;
        case FN:
          led_lighting_mode = LED_MODE_NORMAL;
          led_animation_breathing = false;
          gcr_desired = LED_GCR_MAX;
          break;
        case META:
          led_lighting_mode = LED_MODE_NORMAL;
          led_animation_breathing = false;
          gcr_desired = 40;
          break;
        case OFF:
          led_lighting_mode = LED_MODE_NON_KEYS_ONLY;
          led_animation_breathing = true;
          gcr_breathe = gcr_desired;
          led_animation_breathe_cur = BREATHE_MIN_STEP;
          led_animation_speed = 1.0f;
          breathe_dir = 1;
          break;
    }
    return state;
};

